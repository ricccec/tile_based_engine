    private void areaLavoroPannelloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_areaLavoroPannelloMouseClicked
        this.colonnaClick = this.setClick(evt.getX() + this.areaLavoroPannello.getXScroll());
        this.rigaClick = this.setClick(evt.getY() + this.areaLavoroPannello.getYScroll());
        //this.jLabel1.setText(String.valueOf(colonnaClick) + ", " + String.valueOf(rigaClick));
        
        if (this.land != null){
            if ((this.colonnaClick < this.land.getColsCount())&&(this.rigaClick < this.land.getRowsCount())){ //Se č stato fatto click in un punto interno alla land
                if (evt.getButton() == 1){ //Se č stato fatto click col tasto sx
                    if (this.areaLavoroPannello.getComponenteInUso()!= null){
                        this.land.componenti[colonnaClick][rigaClick] = new Componente(this.areaLavoroPannello.getComponenteInUso());
                        switch (this.land.componenti[colonnaClick][rigaClick].tipo){
                            case 2 : 
                                SetPorta setPorta = new SetPorta(new javax.swing.JFrame(), true, this.land.componenti[colonnaClick][rigaClick], this);
                                break;
                            case 3 : 
                                this.land.componenti[colonnaClick][rigaClick].testo = JOptionPane.showInputDialog(null, "Immettere il testo del cartello", "Testo del cartello", JOptionPane.PLAIN_MESSAGE);
                                break;
                            case 6 : 
                                this.land.componenti[colonnaClick][rigaClick].prezzo = Integer.parseInt(JOptionPane.showInputDialog(null, "Immettere il prezzo di uso del Check Point", "Prezzo del Check Point", JOptionPane.PLAIN_MESSAGE));
                                break;
                        }
                    } else {
                        this.areaLavoroPannello.setComponenteInUso(this.land.componenti[colonnaClick][rigaClick]);
                        this.land.componenti[colonnaClick][rigaClick] = null;
                    }
                    this.areaLavoroPannello.setLand(this.land);
                }
                else //Se non č stato fatto click col tasto sx
                    if (evt.getButton() == 3) //Se č stato fatto click col tasto dx
                        if (this.land.componenti[colonnaClick][rigaClick] != null){
                            this.areaLavoroPannello.setComponenteInUso(null);
                            new AttributiComponente(new javax.swing.JFrame(), true, this.land.componenti[colonnaClick][rigaClick], this);
                            this.areaLavoroPannello.setLand(this.land);
                        }
            }else//Se č stato fatto click in un punto esterno alla land
                if (evt.getButton() == 1)
                    this.areaLavoroPannello.setComponenteInUso(null);
        }
        repaint();
    }//GEN-LAST:event_areaLavoroPannelloMouseClicked

