    private void pannelloAnteprimaLandMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pannelloAnteprimaLandMouseClicked
        if (evt.getButton() == 1){ //Se č stato fatto click col tasto sx
            if (this.jComboBoxLand.getSelectedIndex() != 0){
                int colonna = this.setClick(evt.getX() + this.pannelloAnteprimaLand.getXScroll());
                int riga = this.setClick(evt.getY() + this.pannelloAnteprimaLand.getYScroll());
                if ((colonna < this.land[this.jComboBoxLand.getSelectedIndex() - 1].getColsCount())&&(riga < this.land[this.jComboBoxLand.getSelectedIndex() - 1].getRowsCount())){
                        this.jTextFieldColonna.setText(String.valueOf(colonna));
                        this.jTextFieldRiga.setText(String.valueOf(riga));
                }
            }  
        }
        repaint();
    }//GEN-LAST:event_pannelloAnteprimaLandMouseClicked

    private void jComboBoxLandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxLandActionPerformed
      
        if (this.jComboBoxLand.getSelectedIndex() != 0){
            this.settaBarreScorrimento(this.land[this.jComboBoxLand.getSelectedIndex() - 1]);
            this.pannelloAnteprimaLand.setLand(this.land[this.jComboBoxLand.getSelectedIndex() - 1]);
        }
        else{
            this.jScrollBarOriz.setEnabled(false);
            this.jScrollBarVert.setEnabled(false);
            this.pannelloAnteprimaLand.setLand(null);
        }
        repaint();
    }//GEN-LAST:event_jComboBoxLandActionPerformed

    
