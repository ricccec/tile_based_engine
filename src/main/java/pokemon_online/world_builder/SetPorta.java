    void inizializzaComponenti(){
        
        this.setTitle(componente.nome + " - Set Land destinazione");
        
        elencoFile = new File("land/").list(); //Creo una lista dei file nella dir componenti
        
        /*for (int i = 0; i < elencoFile.length; i++){
            System.out.println(elencoFile[i]);
        }*/
        
        this.land = new Land[elencoFile.length];
        
        for (int i = 0; i < elencoFile.length; i++){
            try{ //Carico i componenti e li memorizzo nell'array land
                FileInputStream fileIn = new FileInputStream("land/" +  this.elencoFile[i]);
                ObjectInputStream objectIn = new ObjectInputStream(fileIn);
                land[i] = (Land)objectIn.readObject();
                objectIn.close(); //Chiudo il flusso
            }catch(IOException e){
                javax.swing.JOptionPane.showMessageDialog(null, "Errore nel caricare la Land: " + e.toString());
            }catch(ClassNotFoundException e){
                javax.swing.JOptionPane.showMessageDialog(null, "Errore nel caricare la Land: " + e.toString());
            }
        }
        
        for (int i = 0; i < elencoFile.length ; i++){
            //Inizializzo la variabile immagine di ogni land
            this.land[i].inizializzaImmaginiComponenti();
            this.jComboBoxLand.addItem(this.land[i].getName());
        }
        
        this.jTextFieldColonna.setText(String.valueOf(this.componente.colonnaArrivo));
        this.jTextFieldRiga.setText(String.valueOf(this.componente.righaArrivo));
        
        if (this.componente.landArrivo != null){//Se il componente ha giŕ settata una land d'arrivo, setta il menů a tendina su quella land
            boolean trovato = false;
            int i = 0;
            while ((trovato == false)&&(i < this.land.length)){
                if (this.componente.landArrivo.equals(this.land[i].getName()) == true){
                    trovato = true;
                    this.jComboBoxLand.setSelectedIndex(i + 1);
                    this.settaBarreScorrimento(this.land[this.jComboBoxLand.getSelectedIndex() - 1]);
                    this.pannelloAnteprimaLand.setLand(this.land[this.jComboBoxLand.getSelectedIndex() - 1]);
                }
                else
                    i++;
            }
        }
    }
    
    void centraFrameSuFrame(Window frame2) { //Posiziona il frame corrente al centro di frame2
        int x = frame2.getX();
        int y = frame2.getY();
        int larghezza = frame2.getWidth();
        int altezza = frame2.getHeight();
        this.setLocation((x + larghezza / 2) - this.getWidth()/2, (y + altezza / 2) - this.getHeight() / 2);
    }
    
    void settaBarreScorrimento(Land land){
        this.pannelloAnteprimaLand.setXScroll(0);
        this.pannelloAnteprimaLand.setYScroll(0);
        this.jScrollBarOriz.setValue(0);
        this.jScrollBarVert.setValue(0);
            //Setto la barra a scorrimento orizzontale
            if (land.getColsCount() * 16 > this.pannelloAnteprimaLand.getWidth()){
                this.jScrollBarOriz.setEnabled(true);
                this.jScrollBarOriz.setMaximum((land.getColsCount() * 16 - this.pannelloAnteprimaLand.getWidth()) + 16);
            }
            else 
                this.jScrollBarOriz.setEnabled(false);
            //Setto la barra a scorrimento verticale
            if (land.getRowsCount() * 16 > this.pannelloAnteprimaLand.getHeight()){
                this.jScrollBarVert.setEnabled(true);
                this.jScrollBarVert.setMaximum((land.getRowsCount() * 16 - this.pannelloAnteprimaLand.getHeight()) + 16);
            }
            else
                this.jScrollBarVert.setEnabled(false);
    }
    
    int setClick(int coordinata){
        //CICLO
        int trovato = 0;
        while (trovato == 0) {
            if (coordinata % 16 == 0) //Se la coordinata č un multiplo di 32
                trovato = 1;
        else //Se la coordinata non č un multiplo di 32
            coordinata--;
        }
        return coordinata/16;
    }
    
    String getNomeLandArrivo(){
        String nome;
        if (this.jComboBoxLand.getSelectedIndex() != 0)
            nome = this.land[this.jComboBoxLand.getSelectedIndex() - 1].getName();
        else
            nome = null;
        return nome;
    }
