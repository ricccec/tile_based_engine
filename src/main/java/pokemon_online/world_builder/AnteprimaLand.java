    void centraFrameSuFrame(Window frame2) { //Posiziona il frame corrente al centro di frame2
        int x = frame2.getX();
        int y = frame2.getY();
        int larghezza = frame2.getWidth();
        int altezza = frame2.getHeight();
        this.setLocation((x + larghezza / 2) - this.getWidth()/2, (y + altezza / 2) - this.getHeight() / 2);
    }
    
    void settaBarreScorrimento(){
            //Setto la barra a scorrimento orizzontale
            if (this.land.getColsCount() * 16 > this.pannelloAnteprimaLand.getWidth()){
                this.jScrollBarOriz.setEnabled(true);
                this.jScrollBarOriz.setMaximum((this.land.getColsCount() * 16 - this.pannelloAnteprimaLand.getWidth()) + 16);
            }
            //Setto la barra a scorrimento verticale
            if (this.land.getRowsCount() * 16 > this.pannelloAnteprimaLand.getHeight()){
                this.jScrollBarVert.setEnabled(true);
                this.jScrollBarVert.setMaximum((this.land.getRowsCount() * 16 - this.pannelloAnteprimaLand.getHeight()) + 16);
            }
    }
    
    int setClick(int coordinata){
        //CICLO
        int trovato = 0;
        while (trovato == 0) {
            if (coordinata % 16 == 0) //Se la coordinata č un multiplo di 32
                trovato = 1;
        else //Se la coordinata non č un multiplo di 32
            coordinata--;
        }
        return coordinata/16;
    }
